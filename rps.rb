    def selection
        print "> "
        choice = gets.strip
        case choice
        when "r"
            @item = "Rock"
        when "p"
            @item = "Paper"
        when "s"
            @item = "Scissors"
        else
            puts "Invalid choice.".colorize(:red)
            selection
        end
        generate_opponent_item
        animation
        puts "PLAY AGAIN? (y/n)".colorize(:orange)
